#!/usr/bin/env node

import fs from 'node:fs';
import path from 'node:path';
import process from 'node:process';
import { fileURLToPath } from 'node:url';

const MIRROR_MARKER = 'AUTO-GENERATED FILE FOR N8N CREATOR PORTAL REPOSITORY VERIFICATION.';
const ALLOWED_FLAGS = new Set(['--check']);

function fail(message) {
	console.error(`mirror:error ${message}`);
	process.exit(1);
}

function ensureRelativeInside(baseDir, targetPath) {
	const relative = path.relative(baseDir, targetPath);
	if (relative.startsWith('..') || path.isAbsolute(relative)) {
		fail(`Resolved path escaped base directory: ${targetPath}`);
	}
}

function walkFiles(rootDir) {
	const files = [];
	if (!fs.existsSync(rootDir)) {
		return files;
	}

	for (const entry of fs.readdirSync(rootDir, { withFileTypes: true })) {
		const fullPath = path.join(rootDir, entry.name);
		if (entry.isDirectory()) {
			files.push(...walkFiles(fullPath));
			continue;
		}
		if (entry.isFile()) {
			files.push(fullPath);
		}
	}

	return files;
}

function removeEmptyParents(filePath, stopDir) {
	let current = path.dirname(filePath);
	const normalizedStopDir = path.resolve(stopDir);

	while (current.startsWith(normalizedStopDir) && current !== normalizedStopDir) {
		if (!fs.existsSync(current)) {
			current = path.dirname(current);
			continue;
		}
		if (fs.readdirSync(current).length > 0) {
			return;
		}
		fs.rmdirSync(current);
		current = path.dirname(current);
	}
}

function buildHeader(sourceRelativePath) {
	return [
		'/*',
		` * ${MIRROR_MARKER}`,
		' *',
		` * Source of truth: packages/n8n-nodes-discourse/${sourceRelativePath}`,
		' *',
		' * Do not edit this file directly.',
		' * Regenerate with: npm --prefix packages/n8n-nodes-discourse run mirror:sync',
		' */',
		'',
	].join('\n');
}

function toSourceRelativePath(distPath) {
	if (!distPath.startsWith('dist/')) {
		fail(`Expected n8n file path to start with "dist/": ${distPath}`);
	}
	if (!distPath.endsWith('.js')) {
		fail(`Expected n8n file path to end with ".js": ${distPath}`);
	}
	return `${distPath.slice('dist/'.length, -'.js'.length)}.ts`;
}

function loadMirrorEntries(packageJson, packageRoot, repoRoot) {
	const n8n = packageJson?.n8n;
	if (!n8n || typeof n8n !== 'object') {
		fail('package.json is missing an "n8n" object.');
	}

	const allDistPaths = [...(n8n.credentials ?? []), ...(n8n.nodes ?? [])];
	if (allDistPaths.length === 0) {
		fail('No n8n.credentials or n8n.nodes entries found in package.json.');
	}

	return allDistPaths.map((distPath) => {
		if (typeof distPath !== 'string') {
			fail(`Invalid n8n path entry (expected string): ${String(distPath)}`);
		}

		const sourceRelativePath = toSourceRelativePath(distPath);
		const sourcePath = path.resolve(packageRoot, sourceRelativePath);
		const targetPath = path.resolve(repoRoot, sourceRelativePath);

		ensureRelativeInside(packageRoot, sourcePath);
		ensureRelativeInside(repoRoot, targetPath);

		if (!fs.existsSync(sourcePath)) {
			fail(`Source file missing for mirror: ${path.relative(repoRoot, sourcePath)}`);
		}

		return {
			distPath,
			sourceRelativePath,
			sourcePath,
			targetPath,
		};
	});
}

function readUtf8(filePath) {
	return fs.readFileSync(filePath, 'utf8');
}

function normalizeFinalNewline(text) {
	return text.endsWith('\n') ? text : `${text}\n`;
}

function syncOrCheck({ checkMode, entries, repoRoot }) {
	let changedCount = 0;
	let issueCount = 0;

	const expectedTargetPaths = new Set(entries.map((entry) => entry.targetPath));

	for (const entry of entries) {
		const sourceContent = readUtf8(entry.sourcePath);
		const generatedContent = normalizeFinalNewline(
			`${buildHeader(entry.sourceRelativePath)}${sourceContent}`,
		);
		const targetExists = fs.existsSync(entry.targetPath);
		const currentContent = targetExists ? readUtf8(entry.targetPath) : null;

		if (!targetExists || currentContent !== generatedContent) {
			if (checkMode) {
				console.error(
					`mirror:out-of-sync ${path.relative(repoRoot, entry.targetPath)} (source: ${path.relative(
						repoRoot,
						entry.sourcePath,
					)})`,
				);
				issueCount += 1;
			} else {
				fs.mkdirSync(path.dirname(entry.targetPath), { recursive: true });
				fs.writeFileSync(entry.targetPath, generatedContent);
				const action = targetExists ? 'updated' : 'created';
				console.log(`mirror:${action} ${path.relative(repoRoot, entry.targetPath)}`);
				changedCount += 1;
			}
		} else {
			console.log(`mirror:ok ${path.relative(repoRoot, entry.targetPath)}`);
		}
	}

	const managedRoots = [path.join(repoRoot, 'credentials'), path.join(repoRoot, 'nodes')];
	const staleManagedFiles = [];

	for (const managedRoot of managedRoots) {
		for (const filePath of walkFiles(managedRoot)) {
			const content = readUtf8(filePath);
			if (!content.includes(MIRROR_MARKER)) {
				continue;
			}
			if (!expectedTargetPaths.has(path.resolve(filePath))) {
				staleManagedFiles.push(path.resolve(filePath));
			}
		}
	}

	for (const staleFilePath of staleManagedFiles) {
		if (checkMode) {
			console.error(`mirror:stale ${path.relative(repoRoot, staleFilePath)}`);
			issueCount += 1;
		} else {
			fs.unlinkSync(staleFilePath);
			removeEmptyParents(staleFilePath, repoRoot);
			console.log(`mirror:removed ${path.relative(repoRoot, staleFilePath)}`);
			changedCount += 1;
		}
	}

	if (checkMode) {
		if (issueCount > 0) {
			fail('Creator Portal mirror is out of sync. Run: npm --prefix packages/n8n-nodes-discourse run mirror:sync');
		}
		console.log('mirror:check-passed');
		return;
	}

	console.log(`mirror:sync-complete ${changedCount} change(s)`);
}

function main() {
	for (const arg of process.argv.slice(2)) {
		if (!ALLOWED_FLAGS.has(arg)) {
			fail(`Unsupported flag: ${arg}`);
		}
	}

	const checkMode = process.argv.includes('--check');
	const thisFilePath = fileURLToPath(import.meta.url);
	const repoRoot = path.resolve(path.dirname(thisFilePath), '..');
	const packageRoot = path.join(repoRoot, 'packages', 'n8n-nodes-discourse');
	const packageJsonPath = path.join(packageRoot, 'package.json');

	if (!fs.existsSync(packageJsonPath)) {
		fail(`Missing package.json: ${path.relative(repoRoot, packageJsonPath)}`);
	}

	const packageJson = JSON.parse(readUtf8(packageJsonPath));
	const entries = loadMirrorEntries(packageJson, packageRoot, repoRoot);
	syncOrCheck({ checkMode, entries, repoRoot });
}

main();
